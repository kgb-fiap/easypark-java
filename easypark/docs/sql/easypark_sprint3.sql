      v_old_json,
      v_new_json
    );

  ELSIF DELETING THEN
    v_old_json := json_reserva(
                    :OLD.id,
                    :OLD.usuario_id,
                    :OLD.vaga_id,
                    :OLD.estado,
                    :OLD.criado_em,
                    :OLD.inicio_previsto,
                    :OLD.duracao_minutos,
                    :OLD.antecedencia_minutos,
                    :OLD.confirmado_em,
                    :OLD.ocupado_em,
                    :OLD.pago_em,
                    :OLD.motivo_cancelamento,
                    :OLD.vaga_bloqueada,
                    :OLD.eta_origem,
                    :OLD.eta_minutos,
                    :OLD.eta_atualizado_em
                  );

    INSERT INTO auditoria_reserva_s3(
      reserva_id, operacao, usuario_banco, data_operacao, dados_old, dados_new
    ) VALUES (
      :OLD.id,
      'DELETE',
      SYS_CONTEXT('USERENV', 'SESSION_USER'),
      SYSTIMESTAMP,
      v_old_json,
      NULL
    );
  END IF;

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20390, 'Falha no trigger de auditoria da reserva: ' || SQLERRM);
END;

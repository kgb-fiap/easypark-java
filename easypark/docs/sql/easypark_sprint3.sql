-- Converte o contexto de uma reserva em um JSON montado manualmente
-- ----------------------------------------------------------------------------
PROMPT ========================================
PROMPT CRIANDO FUNCAO 1 - JSON MANUAL
PROMPT ========================================

CREATE OR REPLACE FUNCTION fn_s3_json_reserva_contexto(
  p_reserva_id            IN NUMBER,
  p_usuario_nome          IN VARCHAR2,
  p_usuario_email         IN VARCHAR2,
  p_estacionamento_nome   IN VARCHAR2,
  p_nivel_nome            IN VARCHAR2,
  p_vaga_codigo           IN VARCHAR2,
  p_estado                IN VARCHAR2,
  p_inicio_previsto       IN TIMESTAMP WITH TIME ZONE,
  p_duracao_minutos       IN NUMBER,
  p_antecedencia_minutos  IN NUMBER,
  p_valor_previsto        IN NUMBER,
  p_valor_final           IN NUMBER
) RETURN CLOB IS
  v_json CLOB;

  FUNCTION esc(p_texto IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN REPLACE(
             REPLACE(
               REPLACE(
                 REPLACE(NVL(p_texto, ''), '\\', '\\\\'),
               '"', '\\"'),
             CHR(10), '\\n'),
           CHR(13), '\\r');
  END;

  FUNCTION txt(p_texto IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    IF p_texto IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN '"' || esc(p_texto) || '"';
  END;

  FUNCTION num(p_numero IN NUMBER) RETURN VARCHAR2 IS
  BEGIN
    IF p_numero IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN TO_CHAR(p_numero, 'FM9999999999999990D9999', 'NLS_NUMERIC_CHARACTERS = '',.''');
  EXCEPTION
    WHEN OTHERS THEN
      RETURN TO_CHAR(p_numero);
  END;

  FUNCTION ts(p_data IN TIMESTAMP WITH TIME ZONE) RETURN VARCHAR2 IS
  BEGIN
    IF p_data IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN '"' || TO_CHAR(p_data, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM') || '"';
  END;

BEGIN
  IF p_reserva_id IS NULL THEN
    RAISE VALUE_ERROR;
  END IF;

  IF p_reserva_id <= 0 THEN
    RAISE NO_DATA_FOUND;
  END IF;

  IF NVL(p_duracao_minutos, 0) < 0 OR NVL(p_antecedencia_minutos, 0) < 0 THEN
    RAISE VALUE_ERROR;
  END IF;

  v_json := '{'
         || '"reservaId":'            || num(p_reserva_id)
         || ',"usuarioNome":'         || txt(p_usuario_nome)
         || ',"usuarioEmail":'        || txt(p_usuario_email)
         || ',"estacionamento":'      || txt(p_estacionamento_nome)
         || ',"nivel":'               || txt(p_nivel_nome)
         || ',"vagaCodigo":'          || txt(p_vaga_codigo)
         || ',"estado":'              || txt(p_estado)
         || ',"inicioPrevisto":'      || ts(p_inicio_previsto)
         || ',"duracaoMinutos":'      || num(p_duracao_minutos)
         || ',"antecedenciaMinutos":' || num(p_antecedencia_minutos)
         || ',"valorPrevisto":'       || num(p_valor_previsto)
         || ',"valorFinal":'          || num(p_valor_final)
         || '}';

  RETURN v_json;

EXCEPTION
  WHEN VALUE_ERROR THEN
    RETURN '{"erro":"Parametros invalidos para gerar JSON da reserva."}';
  WHEN NO_DATA_FOUND THEN
    RETURN '{"erro":"Reserva nao localizada para gerar JSON."}';
  WHEN OTHERS THEN
    RETURN '{"erro":"Falha inesperada ao gerar JSON da reserva: ' || esc(SQLERRM) || '"}';
END;

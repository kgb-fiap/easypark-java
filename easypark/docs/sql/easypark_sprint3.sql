-- ----------------------------------------------------------------------------
-- PROCEDURE 1: JOIN + JSON
-- Gera um relatório de reservas combinando várias tabelas do domínio 
-- e transforma cada linha em JSON com a função criada.
-- ----------------------------------------------------------------------------
PROMPT ========================================
PROMPT CRIANDO PROCEDURE 1 - JOIN + JSON
PROMPT ========================================

CREATE OR REPLACE PROCEDURE prc_s3_rel_reservas_json(
  p_estado     IN VARCHAR2 DEFAULT NULL,
  p_max_linhas IN NUMBER   DEFAULT 10
) IS
  v_json        CLOB;
  v_total       NUMBER;
  v_estado      VARCHAR2(20);
  v_emitidas    NUMBER := 0;
BEGIN
  IF p_max_linhas IS NULL OR p_max_linhas <= 0 THEN
    RAISE VALUE_ERROR;
  END IF;

  v_estado := UPPER(TRIM(p_estado));

  IF v_estado IS NOT NULL
     AND v_estado NOT IN ('PRE_RESERVA','RESERVA','OCUPADA','PAGA','CANCELADA','EXPIRED') THEN
    RAISE VALUE_ERROR;
  END IF;

  SELECT COUNT(*)
    INTO v_total
    FROM reserva r
    JOIN usuario u
      ON u.id = r.usuario_id
    JOIN vaga v
      ON v.id = r.vaga_id
    JOIN nivel n
      ON n.id = v.nivel_id
    JOIN estacionamento e
      ON e.id = n.estacionamento_id
    LEFT JOIN reserva_preco rp
      ON rp.reserva_id = r.id
   WHERE v_estado IS NULL OR r.estado = v_estado;

  IF v_total = 0 THEN
    RAISE NO_DATA_FOUND;
  END IF;

  DBMS_OUTPUT.PUT_LINE('===== RELATORIO JSON DE RESERVAS =====');
  DBMS_OUTPUT.PUT_LINE('Filtro estado: ' || NVL(v_estado, 'TODOS'));
  DBMS_OUTPUT.PUT_LINE('Quantidade encontrada: ' || v_total);
  DBMS_OUTPUT.PUT_LINE('--------------------------------------');

  FOR rec IN (
    SELECT r.id,
           u.nome AS usuario_nome,
           u.email AS usuario_email,
           e.nome AS estacionamento_nome,
           n.nome AS nivel_nome,
           v.codigo AS vaga_codigo,
           r.estado,
           r.inicio_previsto,
           r.duracao_minutos,
           r.antecedencia_minutos,
           rp.valor_previsto,
           rp.valor_final
      FROM reserva r
      JOIN usuario u
        ON u.id = r.usuario_id
      JOIN vaga v
        ON v.id = r.vaga_id
      JOIN nivel n
        ON n.id = v.nivel_id
      JOIN estacionamento e
        ON e.id = n.estacionamento_id
      LEFT JOIN reserva_preco rp
        ON rp.reserva_id = r.id
     WHERE v_estado IS NULL OR r.estado = v_estado
     ORDER BY r.id
  ) LOOP
    EXIT WHEN v_emitidas >= p_max_linhas;

    v_json := fn_s3_json_reserva_contexto(
                rec.id,
                rec.usuario_nome,
                rec.usuario_email,
                rec.estacionamento_nome,
                rec.nivel_nome,
                rec.vaga_codigo,
                rec.estado,
                rec.inicio_previsto,
                rec.duracao_minutos,
                rec.antecedencia_minutos,
                rec.valor_previsto,
                rec.valor_final
              );

    DBMS_OUTPUT.PUT_LINE(DBMS_LOB.SUBSTR(v_json, 32767, 1));
    v_emitidas := v_emitidas + 1;
  END LOOP;

  DBMS_OUTPUT.PUT_LINE('--------------------------------------');
  DBMS_OUTPUT.PUT_LINE('Total emitido: ' || v_emitidas);

EXCEPTION
  WHEN VALUE_ERROR THEN
    DBMS_OUTPUT.PUT_LINE('ERRO: parametros invalidos. Informe um estado valido e p_max_linhas maior que zero.');
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('ERRO: nao ha reservas para o filtro informado.');
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('ERRO inesperado ao gerar relatorio JSON: ' || SQLERRM);
END;
/

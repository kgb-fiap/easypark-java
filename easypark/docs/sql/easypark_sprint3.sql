-- ----------------------------------------------------------------------------
-- FUNÇÃO 2: REGRA DE NEGÓCIO
-- Calcula o valor previsto de uma reserva com base na vaga, duração e
-- antecedência, reaproveitando a tarifa do tipo de vaga cadastrada no projeto.
-- ----------------------------------------------------------------------------
PROMPT ========================================
PROMPT CRIANDO FUNCAO 2 - REGRA DE NEGOCIO
PROMPT ========================================

CREATE OR REPLACE FUNCTION fn_s3_calcular_valor_previsto(
  p_vaga_id               IN NUMBER,
  p_duracao_minutos       IN NUMBER,
  p_antecedencia_minutos  IN NUMBER
) RETURN NUMBER IS
  v_tarifa_por_minuto     tipo_vaga.tarifa_por_minuto%TYPE;
  v_fator_antecedencia    NUMBER(12,6);
  v_valor                 NUMBER(12,4);
  e_duracao_excessiva     EXCEPTION;
BEGIN
  IF p_vaga_id IS NULL OR p_duracao_minutos IS NULL OR p_antecedencia_minutos IS NULL THEN
    RAISE VALUE_ERROR;
  END IF;

  IF p_duracao_minutos <= 0 OR p_antecedencia_minutos < 0 THEN
    RAISE VALUE_ERROR;
  END IF;

  IF p_duracao_minutos > 1440 THEN
    RAISE e_duracao_excessiva;
  END IF;

  SELECT tv.tarifa_por_minuto
    INTO v_tarifa_por_minuto
    FROM vaga v
    JOIN tipo_vaga tv
      ON tv.id = v.tipo_vaga_id
   WHERE v.id = p_vaga_id;

  -- percentual de antecedência aplicado sobre a duração.
  v_fator_antecedencia := 1 + (p_antecedencia_minutos * 0.01);
  v_valor := ROUND(v_tarifa_por_minuto * p_duracao_minutos * v_fator_antecedencia, 4);

  RETURN v_valor;

EXCEPTION
  WHEN VALUE_ERROR THEN
    RAISE_APPLICATION_ERROR(-20301, 'Parametros invalidos para calculo do valor previsto.');
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20302, 'Vaga nao encontrada para calculo do valor previsto.');
  WHEN e_duracao_excessiva THEN
    RAISE_APPLICATION_ERROR(-20303, 'Duracao acima do limite permitido para o calculo (1440 minutos).');
END;

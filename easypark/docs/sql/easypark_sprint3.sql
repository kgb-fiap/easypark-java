AFTER INSERT OR UPDATE OR DELETE ON reserva
FOR EACH ROW
DECLARE
  v_old_json CLOB;
  v_new_json CLOB;

  FUNCTION esc(p_texto IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN REPLACE(
             REPLACE(
               REPLACE(
                 REPLACE(NVL(p_texto, ''), '\\', '\\\\'),
               '"', '\\"'),
             CHR(10), '\\n'),
           CHR(13), '\\r');
  END;

  FUNCTION txt(p_texto IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    IF p_texto IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN '"' || esc(p_texto) || '"';
  END;

  FUNCTION num(p_numero IN NUMBER) RETURN VARCHAR2 IS
  BEGIN
    IF p_numero IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN TO_CHAR(p_numero, 'FM9999999999999990D9999', 'NLS_NUMERIC_CHARACTERS = '',.''');
  END;

  FUNCTION ts(p_data IN TIMESTAMP WITH TIME ZONE) RETURN VARCHAR2 IS
  BEGIN
    IF p_data IS NULL THEN
      RETURN 'null';
    END IF;
    RETURN '"' || TO_CHAR(p_data, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM') || '"';
  END;

  FUNCTION json_reserva(
    p_id                   IN NUMBER,
    p_usuario_id           IN NUMBER,
    p_vaga_id              IN NUMBER,
    p_estado               IN VARCHAR2,
    p_criado_em            IN TIMESTAMP WITH TIME ZONE,
    p_inicio_previsto      IN TIMESTAMP WITH TIME ZONE,

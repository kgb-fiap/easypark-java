SELECT id,
       reserva_id,
       operacao,
       usuario_banco,
       TO_CHAR(data_operacao, 'YYYY-MM-DD HH24:MI:SS TZH:TZM') AS data_operacao,
       DBMS_LOB.SUBSTR(dados_old, 4000, 1) AS dados_old,
       DBMS_LOB.SUBSTR(dados_new, 4000, 1) AS dados_new
  FROM auditoria_reserva_s3

-- ----------------------------------------------------------------------------
-- PROCEDURE 2: ANTERIOR / ATUAL / PRÓXIMO
-- Lê a tabela PAGAMENTO e mostra, para a coluna VALOR, o valor anterior, atual
-- e próximo em cada linha, exibindo "Vazio" quando não houver vizinho.
-- ----------------------------------------------------------------------------
PROMPT ========================================
PROMPT CRIANDO PROCEDURE 2 - ANTERIOR / ATUAL / PROXIMO
PROMPT ========================================

CREATE OR REPLACE PROCEDURE prc_s3_rel_pagamentos_vizinhos(
  p_min_linhas IN NUMBER DEFAULT 5
) IS
  v_total NUMBER;
BEGIN
  IF p_min_linhas IS NULL OR p_min_linhas <= 0 THEN
    RAISE VALUE_ERROR;
  END IF;

  SELECT COUNT(*)
    INTO v_total
    FROM pagamento;

  IF v_total < p_min_linhas THEN
    RAISE NO_DATA_FOUND;
  END IF;

  DBMS_OUTPUT.PUT_LINE('===== RELATORIO PAGAMENTOS (ANTERIOR / ATUAL / PROXIMO) =====');
  DBMS_OUTPUT.PUT_LINE(RPAD('ID_PGTO', 12)
                    || RPAD('ID_USUARIO', 12)
                    || RPAD('ANTERIOR', 18)
                    || RPAD('ATUAL', 18)
                    || RPAD('PROXIMO', 18));
  DBMS_OUTPUT.PUT_LINE(RPAD('-', 78, '-'));

  FOR rec IN (
    SELECT id,
           usuario_id,
           NVL(TO_CHAR(LAG(valor) OVER (ORDER BY id), 'FM9999990D0000', 'NLS_NUMERIC_CHARACTERS = '',.'''), 'Vazio') AS anterior,
           TO_CHAR(valor, 'FM9999990D0000', 'NLS_NUMERIC_CHARACTERS = '',.''') AS atual,
           NVL(TO_CHAR(LEAD(valor) OVER (ORDER BY id), 'FM9999990D0000', 'NLS_NUMERIC_CHARACTERS = '',.'''), 'Vazio') AS proximo
      FROM pagamento
     ORDER BY id
  ) LOOP
    DBMS_OUTPUT.PUT_LINE(RPAD(TO_CHAR(rec.id), 12)
                      || RPAD(NVL(TO_CHAR(rec.usuario_id), 'Vazio'), 12)
                      || RPAD(rec.anterior, 18)
                      || RPAD(rec.atual, 18)
                      || RPAD(rec.proximo, 18));
  END LOOP;

EXCEPTION
  WHEN VALUE_ERROR THEN
    DBMS_OUTPUT.PUT_LINE('ERRO: p_min_linhas deve ser maior que zero.');
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('ERRO: a tabela PAGAMENTO nao possui a quantidade minima de linhas para o relatorio.');
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('ERRO inesperado ao gerar relatorio de pagamentos: ' || SQLERRM);
END;
/
SHOW ERRORS


-- ----------------------------------------------------------------------------
-- TESTE 5
-- Cria, altera e remove uma reserva de teste para comprovar que o trigger gera
-- registros de auditoria para INSERT, UPDATE e DELETE.
-- ----------------------------------------------------------------------------
PROMPT ========================================
PROMPT TESTE 5 - TRIGGER DE AUDITORIA (INSERT / UPDATE / DELETE)
PROMPT ========================================

DECLARE
  v_usuario_id   NUMBER;
  v_vaga_id      NUMBER;
  v_reserva_id   NUMBER;
BEGIN
  SELECT MIN(id)
    INTO v_usuario_id
    FROM usuario;

  SELECT MIN(id)
    INTO v_vaga_id
    FROM vaga;

  INSERT INTO reserva(
    usuario_id,
    vaga_id,
    estado,
    criado_em,
    inicio_previsto,
    duracao_minutos,
    antecedencia_minutos,
    motivo_cancelamento,
    vaga_bloqueada,
    eta_origem,
    eta_minutos,
    eta_atualizado_em
  ) VALUES (
    v_usuario_id,
    v_vaga_id,
    'CANCELADA',
    SYSTIMESTAMP,
    SYSTIMESTAMP + INTERVAL '2' HOUR,
    45,
    10,
    'Registro de teste da auditoria Sprint 3',
    'N',
    NULL,
    NULL,
    NULL
  )
  RETURNING id INTO v_reserva_id;

  UPDATE reserva
     SET motivo_cancelamento  = 'Registro de teste atualizado pela Sprint 3',
         antecedencia_minutos = 15,
         eta_origem           = 'manual',
         eta_minutos          = 12,
         eta_atualizado_em    = SYSTIMESTAMP
   WHERE id = v_reserva_id;

  DELETE FROM reserva
   WHERE id = v_reserva_id;

  COMMIT;

  DBMS_OUTPUT.PUT_LINE('Reserva de teste usada na auditoria: ' || v_reserva_id);
END;
/


    v_new_json := json_reserva(
                    :NEW.id,
                    :NEW.usuario_id,
                    :NEW.vaga_id,
                    :NEW.estado,
                    :NEW.criado_em,
                    :NEW.inicio_previsto,
                    :NEW.duracao_minutos,
                    :NEW.antecedencia_minutos,
                    :NEW.confirmado_em,
                    :NEW.ocupado_em,
                    :NEW.pago_em,
                    :NEW.motivo_cancelamento,
                    :NEW.vaga_bloqueada,
                    :NEW.eta_origem,
                    :NEW.eta_minutos,
                    :NEW.eta_atualizado_em
                  );

    INSERT INTO auditoria_reserva_s3(
      reserva_id, operacao, usuario_banco, data_operacao, dados_old, dados_new
    ) VALUES (
      :NEW.id,
      'INSERT',
      SYS_CONTEXT('USERENV', 'SESSION_USER'),
      SYSTIMESTAMP,
      NULL,
      v_new_json
    );

  ELSIF UPDATING THEN
    v_old_json := json_reserva(
                    :OLD.id,
                    :OLD.usuario_id,
                    :OLD.vaga_id,
                    :OLD.estado,
                    :OLD.criado_em,
                    :OLD.inicio_previsto,
                    :OLD.duracao_minutos,
                    :OLD.antecedencia_minutos,
                    :OLD.confirmado_em,
                    :OLD.ocupado_em,
                    :OLD.pago_em,
                    :OLD.motivo_cancelamento,
                    :OLD.vaga_bloqueada,
                    :OLD.eta_origem,
                    :OLD.eta_minutos,
                    :OLD.eta_atualizado_em
                  );

    v_new_json := json_reserva(
                    :NEW.id,
                    :NEW.usuario_id,
                    :NEW.vaga_id,
                    :NEW.estado,
                    :NEW.criado_em,
                    :NEW.inicio_previsto,
                    :NEW.duracao_minutos,
                    :NEW.antecedencia_minutos,
                    :NEW.confirmado_em,
                    :NEW.ocupado_em,
                    :NEW.pago_em,
                    :NEW.motivo_cancelamento,
                    :NEW.vaga_bloqueada,
                    :NEW.eta_origem,
                    :NEW.eta_minutos,
                    :NEW.eta_atualizado_em
                  );

    INSERT INTO auditoria_reserva_s3(

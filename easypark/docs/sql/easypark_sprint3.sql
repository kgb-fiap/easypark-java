    p_antecedencia_minutos IN NUMBER,
    p_confirmado_em        IN TIMESTAMP WITH TIME ZONE,
    p_ocupado_em           IN TIMESTAMP WITH TIME ZONE,
    p_pago_em              IN TIMESTAMP WITH TIME ZONE,
    p_motivo_cancelamento  IN VARCHAR2,
    p_vaga_bloqueada       IN CHAR,
    p_eta_origem           IN VARCHAR2,
    p_eta_minutos          IN NUMBER,
    p_eta_atualizado_em    IN TIMESTAMP WITH TIME ZONE
  ) RETURN CLOB IS
    v_json CLOB;
  BEGIN
    v_json := '{'
           || '"id":'                   || num(p_id)
           || ',"usuarioId":'          || num(p_usuario_id)
           || ',"vagaId":'             || num(p_vaga_id)
           || ',"estado":'             || txt(p_estado)
           || ',"criadoEm":'           || ts(p_criado_em)
           || ',"inicioPrevisto":'     || ts(p_inicio_previsto)
           || ',"duracaoMinutos":'     || num(p_duracao_minutos)
           || ',"antecedenciaMinutos":'|| num(p_antecedencia_minutos)
           || ',"confirmadoEm":'       || ts(p_confirmado_em)
           || ',"ocupadoEm":'          || ts(p_ocupado_em)
           || ',"pagoEm":'             || ts(p_pago_em)
           || ',"motivoCancelamento":' || txt(p_motivo_cancelamento)
           || ',"vagaBloqueada":'      || txt(p_vaga_bloqueada)
           || ',"etaOrigem":'          || txt(p_eta_origem)
           || ',"etaMinutos":'         || num(p_eta_minutos)
           || ',"etaAtualizadoEm":'    || ts(p_eta_atualizado_em)
           || '}';
    RETURN v_json;
  END;
BEGIN

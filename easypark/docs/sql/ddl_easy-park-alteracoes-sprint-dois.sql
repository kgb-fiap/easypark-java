-- ITEM 6: Função com regra de negócio + INNER JOIN + ORDER BY + agregações
-- Regra: faturamento por dia/estacionamento para pagamentos com status 'SUCCEEDED'
--------------------------------------------------------------------------------

CREATE OR REPLACE TYPE t_fat_row AS OBJECT (
  dia              DATE,
  estacionamento   VARCHAR2(250),
  qtd_transacoes   NUMBER,
  valor_total      NUMBER(12,2)
);
/
CREATE OR REPLACE TYPE t_fat_tab AS TABLE OF t_fat_row;
/

CREATE OR REPLACE FUNCTION fn_rel_faturamento(
  p_ini IN DATE,
  p_fim IN DATE
) RETURN t_fat_tab IS
  v_res t_fat_tab := t_fat_tab();
  CURSOR c IS
    SELECT TRUNC(p.criado_em) AS dia, e.nome AS estacionamento,
           COUNT(*) AS qtd, SUM(p.valor) AS total
      FROM pagamento p
      INNER JOIN reserva r       ON r.id = p.reserva_id
      INNER JOIN vaga v          ON v.id = r.vaga_id
      INNER JOIN nivel n         ON n.id = v.nivel_id
      INNER JOIN estacionamento e ON e.id = n.estacionamento_id
     WHERE p.status = 'SUCCEEDED'
       AND TRUNC(p.criado_em) BETWEEN p_ini AND p_fim
     GROUP BY TRUNC(p.criado_em), e.nome
     ORDER BY dia DESC, estacionamento;
  v_row c%ROWTYPE;
BEGIN
  FOR v_row IN c LOOP
    v_res.EXTEND;
    v_res(v_res.LAST) := t_fat_row(v_row.dia, v_row.estacionamento, v_row.qtd, NVL(v_row.total,0));
  END LOOP;
  RETURN v_res;
END;
/

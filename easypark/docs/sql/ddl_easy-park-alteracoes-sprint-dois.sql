-- RESERVA (usa fn_validar_antecedencia)
CREATE OR REPLACE PROCEDURE reserva_ins(
  p_usuario_id           IN NUMBER,
  p_vaga_id              IN NUMBER,
  p_inicio_previsto      IN TIMESTAMP WITH TIME ZONE,
  p_duracao_minutos      IN NUMBER,
  p_antecedencia_minutos IN NUMBER,
  p_id                   OUT NUMBER
) IS
  v_estac_id NUMBER;
  v_ok       NUMBER;
BEGIN
  -- Descobre o estacionamento da vaga
  SELECT n.estacionamento_id
    INTO v_estac_id
    FROM vaga v
    JOIN nivel n ON n.id = v.nivel_id
   WHERE v.id = p_vaga_id;

  -- Valida antecedência para este estacionamento/usuário
  v_ok := fn_validar_antecedencia(v_estac_id, p_usuario_id, p_antecedencia_minutos);
  IF v_ok = 0 THEN
    raise_application_error(-20001,'Antecedência inválida para este estacionamento/usuário.');
  END IF;

  -- Cria PRE_RESERVA
  INSERT INTO reserva(
      usuario_id, vaga_id, estado, criado_em,
      inicio_previsto, duracao_minutos, antecedencia_minutos,
      eta_origem, eta_minutos, eta_atualizado_em, vaga_bloqueada
  )
  VALUES (
      p_usuario_id, p_vaga_id, 'PRE_RESERVA', SYSTIMESTAMP,
      p_inicio_previsto, p_duracao_minutos, p_antecedencia_minutos,
      NULL, NULL, NULL, 'N'
  )
  RETURNING id INTO p_id;

  -- Histórico da criação (ATENÇÃO: use apenas as colunas que EXISTEM no seu DDL)
  INSERT INTO reserva_hist(
      reserva_id, from_estado, to_estado, origem_evento, referencia_id, observacao, ocorrido_em
  ) VALUES (
      p_id, NULL, 'PRE_RESERVA', 'MANUAL', NULL, 'criação', SYSTIMESTAMP
  );

EXCEPTION
  WHEN NO_DATA_FOUND THEN
    raise_application_error(-20001,'Vaga ou estacionamento inexistente.');
  WHEN OTHERS THEN
    raise_application_error(-20003,'Falha ao inserir reserva: '||SQLERRM);
END;
/

CREATE OR REPLACE PROCEDURE reserva_upd(
  p_id IN NUMBER,
  p_inicio_previsto IN TIMESTAMP WITH TIME ZONE,
  p_duracao_minutos IN NUMBER,
  p_antecedencia_minutos IN NUMBER
) IS
  v_user NUMBER; v_vaga NUMBER; v_estac NUMBER; v_ok NUMBER;
BEGIN
  SELECT usuario_id, vaga_id INTO v_user, v_vaga FROM reserva WHERE id = p_id FOR UPDATE;
  SELECT n.estacionamento_id INTO v_estac
    FROM vaga v JOIN nivel n ON n.id = v.nivel_id
   WHERE v.id = v_vaga;

  v_ok := fn_validar_antecedencia(v_estac, v_user, p_antecedencia_minutos);
  IF v_ok = 0 THEN
    raise_application_error(-20001,'Antecedência inválida para este estacionamento/usuário.');
  END IF;

  UPDATE reserva
     SET inicio_previsto = p_inicio_previsto,
         duracao_minutos = p_duracao_minutos,
         antecedencia_minutos = p_antecedencia_minutos
   WHERE id = p_id;
  IF SQL%ROWCOUNT = 0 THEN raise_application_error(-20002,'Reserva não encontrada.'); END IF;
END;
/
CREATE OR REPLACE PROCEDURE reserva_del(p_id IN NUMBER) IS
  v_estado VARCHAR2(20);
BEGIN
  SELECT estado INTO v_estado FROM reserva WHERE id = p_id FOR UPDATE;
  IF v_estado IN ('OCUPADA','PAGA') THEN
    raise_application_error(-20003,'Não é permitido excluir reserva OCUPADA/PAGA.');
  END IF;
  DELETE FROM reserva WHERE id = p_id;
END;

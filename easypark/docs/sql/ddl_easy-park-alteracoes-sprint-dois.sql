-- 2.1 Validação CPF/CNPJ: retorna 1 (ok) / 0 (inválido)
CREATE OR REPLACE FUNCTION fn_validar_cpf_cnpj(p_doc IN VARCHAR2)
RETURN NUMBER DETERMINISTIC IS
BEGIN
  IF p_doc IS NULL THEN RETURN 0; END IF;
  IF REGEXP_LIKE(p_doc, '^[0-9]{11}$') THEN
    RETURN 1;
  ELSIF REGEXP_LIKE(p_doc, '^[0-9]{14}$') THEN
    RETURN 1;
  ELSE
    RETURN 0;
  END IF;
END;
/
SHOW ERRORS

-- 2.2 Validação de e-mail: retorna 1 (ok) / 0 (inválido)
CREATE OR REPLACE FUNCTION fn_validar_email(p_email IN VARCHAR2)
RETURN NUMBER DETERMINISTIC IS
BEGIN
  IF p_email IS NULL THEN RETURN 0; END IF;
  IF REGEXP_LIKE(p_email, '^[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}$') THEN
    RETURN 1;
  ELSE
    RETURN 0;
  END IF;
END;
/
SHOW ERRORS

-- 2.3 Validação de antecedência escolhida pelo usuário (respeita regras do estacionamento e suspensão)
--     Retorna 1 (ok) / 0 (inválido)
CREATE OR REPLACE FUNCTION fn_validar_antecedencia(
  p_estacionamento_id   IN NUMBER,
  p_usuario_id          IN NUMBER,
  p_antecedencia_min    IN NUMBER
) RETURN NUMBER IS
  v_max_normal    NUMBER;
  v_max_suspenso  NUMBER;
  v_susp_ate      TIMESTAMP WITH TIME ZONE;
  v_hoje          TIMESTAMP WITH TIME ZONE := SYSTIMESTAMP;
BEGIN
  IF p_antecedencia_min IS NULL OR p_antecedencia_min < 0 THEN
    RETURN 0;
  END IF;

  SELECT max_antecedencia_minutos, max_antecedencia_minutos_suspenso
    INTO v_max_normal, v_max_suspenso
    FROM estacionamento
   WHERE id = p_estacionamento_id;

  SELECT suspensao_ate
    INTO v_susp_ate
    FROM usuario
   WHERE id = p_usuario_id;

  IF v_susp_ate IS NOT NULL AND v_susp_ate > v_hoje THEN
    RETURN CASE WHEN p_antecedencia_min <= NVL(v_max_suspenso, v_max_normal) THEN 1 ELSE 0 END;
  ELSE
    RETURN CASE WHEN p_antecedencia_min <= v_max_normal THEN 1 ELSE 0 END;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RETURN 0;
END;
/
SHOW ERRORS


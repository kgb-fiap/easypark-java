-- ITEM 5: Função com cursor + JOINs para relatório
-- Saída: tabela com reservas e contexto (usuario, vaga, estacionamento)
--------------------------------------------------------------------------------

-- Tipos SQL
CREATE OR REPLACE TYPE t_rel_reserva_row AS OBJECT (
  reserva_id        NUMBER,
  usuario_email     VARCHAR2(320),
  estacionamento    VARCHAR2(250),
  vaga_codigo       VARCHAR2(50),
  estado            VARCHAR2(20),
  criado_em         TIMESTAMP WITH TIME ZONE
);
/
CREATE OR REPLACE TYPE t_rel_reserva_tab AS TABLE OF t_rel_reserva_row;
/

-- Função que retorna a tabela 
CREATE OR REPLACE FUNCTION fn_rel_reservas(
  p_ini IN TIMESTAMP WITH TIME ZONE,
  p_fim IN TIMESTAMP WITH TIME ZONE
) RETURN t_rel_reserva_tab IS
  v_res t_rel_reserva_tab := t_rel_reserva_tab();
  CURSOR c IS
    SELECT r.id, u.email, e.nome, v.codigo, r.estado, r.criado_em
      FROM reserva r
      JOIN usuario u       ON u.id = r.usuario_id
      JOIN vaga v          ON v.id = r.vaga_id
      JOIN nivel n         ON n.id = v.nivel_id
      JOIN estacionamento e ON e.id = n.estacionamento_id
     WHERE r.criado_em BETWEEN p_ini AND p_fim
     ORDER BY r.criado_em DESC, e.nome, v.codigo;
  v_row c%ROWTYPE;
BEGIN
  FOR v_row IN c LOOP
    v_res.EXTEND;
    v_res(v_res.LAST) := t_rel_reserva_row(
      v_row.id, v_row.email, v_row.nome, v_row.codigo, v_row.estado, v_row.criado_em
    );
  END LOOP;
  RETURN v_res;
END;
/

-- UF
CREATE OR REPLACE PROCEDURE uf_ins(p_sigla IN VARCHAR2, p_nome IN VARCHAR2) IS
BEGIN
  IF p_sigla IS NULL OR NOT REGEXP_LIKE(p_sigla,'^[A-Z]{2}$') THEN
    raise_application_error(-20001,'UF inválida: use duas letras maiúsculas.');
  END IF;
  INSERT INTO uf(sigla, nome) VALUES (p_sigla, p_nome);
EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
  raise_application_error(-20001,'UF já existente.');
END;
/
CREATE OR REPLACE PROCEDURE uf_upd(p_sigla IN VARCHAR2, p_nome IN VARCHAR2) IS
BEGIN
  UPDATE uf SET nome = p_nome WHERE sigla = p_sigla;
  IF SQL%ROWCOUNT = 0 THEN raise_application_error(-20002,'UF não encontrada.'); END IF;
END;
/
CREATE OR REPLACE PROCEDURE uf_del(p_sigla IN VARCHAR2) IS
BEGIN
  DELETE FROM uf WHERE sigla = p_sigla;
  IF SQL%ROWCOUNT = 0 THEN raise_application_error(-20002,'UF não encontrada.'); END IF;
END;
